export enum Routes {  // Виды страниц
  LOGIN = 0,  // Страница идентификации (логирования)
  CREATE = 1, // Страница создания сущностей
  READ = 2, // Страница, на которой можно просматривать отдельные записи
  EDIT = 3, // Страница, на которой можно редактировать отдельные записи
  DELETE = 4, // Страница, с помощью которой можно удалять записи
  VIEW = 5, // Страница, на которой записи выводятся в виде таблицы
  ANALYSE = 6 // Страница для анализа данных, создания запросов
}
